public class Question633 {
    // 先给c开方取整，减少计算范围
    // 两头取平方，之和与c比较，大于j-1，小于i+1；直到i>j
    public boolean judgeSquareSum(int c) {
        int i = 0, j = (int) Math.sqrt(c);
        while (i <= j) {
            int powSum = i * i + j * j;
            if (powSum == c) {
                return true;
            } else if (powSum > c) {
                j--;
            } else {
                i++;
            }
        }
        return false;
    }
}
 